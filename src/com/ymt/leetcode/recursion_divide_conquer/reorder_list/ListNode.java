package com.ymt.leetcode.recursion_divide_conquer.reorder_list;

/**
 * @Description
 * @author yumingtao
 * @date 2022-04-23 15:46
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
