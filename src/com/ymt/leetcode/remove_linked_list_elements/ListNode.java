package com.ymt.leetcode.remove_linked_list_elements;

/**
 * @Description
 * @author yumingtao
 * @date 2022-04-18 21:16
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
