package com.ymt.leetcode.list.remove_linked_list_elements;

/**
 * @description
 * @author yumingtao
 * @date 2022-07-30 18:24
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
