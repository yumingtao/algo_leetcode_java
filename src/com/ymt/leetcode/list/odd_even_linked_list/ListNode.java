package com.ymt.leetcode.list.odd_even_linked_list;

/**
 * @description
 * @author yumingtao
 * @date 2022-07-31 19:31
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
